def carré(x,y,c): # Cette fonction fait le carré qui trace les contours du dé
	goto(x,y)
	down()
	for i in range(4):
		forward(c)
		left(90)
	up()


def point(x,y,c): # Cette fonction dessine un point
	begin_fill()
	carré(x,y,c)
	end_fill()


def lanceDé(): # Cette fonction lance le dé et retourne le numéro sur lequel il tombe
	n = randint(1,6)
	return n



